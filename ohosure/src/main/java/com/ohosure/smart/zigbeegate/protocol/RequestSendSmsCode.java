public class RequestSendSmsCode extends HttpOutMessage {
    public RequestSendSmsCode(String session, String phoneNumber, String tmplType) {

        String value = "{  \"parameter\" : { \"phoneNumber\" : \"" + phoneNumber
                + "\",\"tmplType\": \"" + tmplType
                + "\"}, \"sessionID\" : \"" + session
                + "\", \"system\" : {  \"type\" : \"" + hostIdentity + "\" } }";

        sb.append("POST /requestSendSmsCode HTTP/1.1\r\n")
                .append("Accept: */*\r\n")
                .append("Connection: keep-alive\r\n")
                .append("Content-Length: " + value.length() + "\r\n")
                .append("\r\n").append(value);

    }
}
